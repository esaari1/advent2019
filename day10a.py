asteroids = []

def gcd(a, b):
	if b == 0:
		return a
	return gcd(b, a % b)

def delta(a, b):
	xd = abs(a[0] - b[0])
	yd = abs(a[1] - b[1])

	xm = 1
	ym = 1

	if a[0] > b[0]:
		xm = -1
	if a[1] > b[1]:
		ym = -1	

	if xd == 0:
		return [0, ym]
	if yd == 0:
		return [xm, 0]

	g = gcd(xd, yd)
	return [xm * xd / g, ym * yd / g]


def canSee(a, b, d):
	s = a[:]
	s[0] += d[0]
	s[1] += d[1]

	while s != b:
		if s in asteroids:
			return False
		s[0] += d[0]
		s[1] += d[1]

	return True

